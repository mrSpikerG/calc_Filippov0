#pragma once

inline int plus(int firstNum, int secondNum) {
	return firstNum + secondNum;
}

inline int minus(int firstNum, int secondNum) {
	return firstNum - secondNum;
}

inline int mul(int firstNum, int secondNum) {
	return firstNum * secondNum;
}

inline int div(int firstNum, int secondNum) {
	return firstNum / secondNum;
}
